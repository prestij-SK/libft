#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*temp;

	temp = NULL;
	while (*s)
	{
		if (*s == (char)c)
			temp = (char *)s;
		++s;
	}
	if (*s == c)
		temp = (char *)s;
	return (temp);
}
